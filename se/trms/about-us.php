<h2>WELCOME TO COIMBATORE CAMPUS!</h2>

<p>The beginnings of Amrita Vishwa Vidyapeetham can be traced to 1994 when a School of Engineering was started in an obscure village named Ettimadai, at the foothills of Bouluvanpatty ranges of the Western Ghats in the Coimbatore district of Tamil Nadu. As Amrita's first campus, Coimbatore is home to the administrative headquarters of Amrita Vishwa Vidyapeetham.</p>

<p>A lot of emphasis is placed on eco-friendly and conservation practices. Waste water from the hostels and faculty and staff quarters is treated using EM; no chemicals are used in the process. Organic fertilizers are made using vermi-compost, pesticides have been avoided since the very beginning.</p>

<p>The institution's Coimbatore campus is home to Schools of <a href="/school/agriculture">Agriculture Sciences</a>, <a href="/school/business/coimbatore">Business</a>, <a href="/school/communication">Mass Communication</a>, <a href="/school/engineering/coimbatore">Engineering</a> and <a href="/school/engineering/coimbatore">Social Work.</a></p>

